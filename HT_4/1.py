"""
1. Написати функцію < square > , яка прийматиме один аргумент - сторону квадрата,
   і вертатиме 3 значення (кортеж):
   периметр квадрата,
   площа квадрата
   діагональ.
"""


def square(x):
    p = 4 * x
    s = x ** 2
    d = (x ** 2 + x ** 2) ** 0.5
    return p, s, d


x1 = int(input("Ведіть сторону квадрата: "))
print(square(x1))
